// Helper function to get random element
export function getRandomElement<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

// Helper function to get weighted random element
export function getWeightedRandomElement<T extends { percentage: number }>(arr: T[]): T {
  const totalWeight = arr.reduce((sum, item) => sum + item.percentage, 0);
  let random = Math.random() * totalWeight;

  for (const item of arr) {
    random -= item.percentage;
    if (random <= 0) return item;
  }

  return arr[arr.length - 1];
}

