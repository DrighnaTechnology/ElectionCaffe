// Generate random date of birth (age between 18 and 95)
export function generateDOB(minAge: number = 18, maxAge: number = 95): Date {
  const today = new Date();
  const age = Math.floor(minAge + Math.random() * (maxAge - minAge));
  const birthYear = today.getFullYear() - age;
  const birthMonth = Math.floor(Math.random() * 12);
  const birthDay = Math.floor(1 + Math.random() * 28);
  return new Date(birthYear, birthMonth, birthDay);
}

// Calculate age from date
export function calculateAge(dob: Date): number {
  const today = new Date();
  let age = today.getFullYear() - dob.getFullYear();
  const monthDiff = today.getMonth() - dob.getMonth();
  if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < dob.getDate())) {
    age--;
  }
  return age;
}
